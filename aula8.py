adicao = 10 + 10
print('Adição:', adicao)

subtracao = 6 - 2
print('Subtração:', subtracao)

multiplicacao = 2 * 5
print('Multiplicação:', multiplicacao)

divisao = 10 / 2.2  # sempre retorna float
print('Divisão:', divisao)

divisao_inteira = 10 // 2.2  # Não retorna um número inteiro como o nome diz, mas ele corta todos os números após a vírgula como no exemplo
print('Divisão Inteira:', divisao_inteira)

exponenciacao = 2 ** 5
print('Exponenciação:', exponenciacao)

modulo = 55 % 2  # Resto da divisão
print('Módulo:', modulo)
