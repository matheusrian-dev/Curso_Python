# Tipo de dado bool (boolean)
# Ao questionar algo em um programa só existem duas respostas possíveis: True e False
# Existem vários operadores para "questionar", como por exemplo o ==, que é um operador lógico que questiona se um valor é igual ao outro
print(10 == 10) # True
print(0 > 2) # False
print(type(10==10))
print(type(True))
# diferente de outras linguas como C#, True e False são escritos com a primeira letra maiúscula.