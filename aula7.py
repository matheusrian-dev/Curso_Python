# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar números e underline (_).
# O sinal de = é o operador de atribuição. ele é usado para atribuir um valor a um nome(variável).
# Uso: nome_variavel = expressão
# Variáveis não são utilizadas para abreviar código mas para torná-lo mais legível e menos repetitivo
nome = 'Thainá'
idade = 17
maior_de_idade = idade >= 18
pet_favorito = "Coelho"
print('Nome:', nome)
print('Idade:', idade)
print('É maior de idade?', maior_de_idade)
print('Pet Favorito:', pet_favorito)